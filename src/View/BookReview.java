@Override
public Component getListCellRendererComponent(JList<? extends Book> list, Book book, int index, boolean isSelected, boolean cellHasFocus) {
    
    // --- Asigna los datos a tus componentes visuales ---
    titleLabel.setText("<html><b>" + book.getName() + "</b><br>" + book.getAuthorName() + "</html>");
    
    Review review = book.getReview(); // Obtenemos la reseña del libro
    if (review != null) {
        // Si hay reseña, la mostramos
        ratingLabel.setText(generateStars(review.getRating()));
        commentArea.setText("“" + review.getComment() + "”");
        commentArea.setVisible(true);
    } else {
        // Si no hay reseña, ocultamos los componentes
        ratingLabel.setText(""); // Sin estrellas
        commentArea.setText("");
        commentArea.setVisible(false);
    }

    // --- Maneja los colores de selección ---
    if (isSelected) {
        setBackground(list.getSelectionBackground());
    } else {
        setBackground(list.getBackground());
    }

    return this;
}

// Pega este método de utilidad también
private String generateStars(int rating) {
    StringBuilder stars = new StringBuilder();
    for (int i = 0; i < 5; i++) {
        if (i < rating) {
            stars.append("★"); // Estrella rellena
        } else {
            stars.append("☆"); // Estrella vacía
        }
    }
    return stars.toString();
}

